package com;

public class Admin {

	String admin_phone;
	String admin_name;
	String admin_password;
	String admin_birthday;
	String admin_sex;
	String admin_email;
	 	
	public Admin(){
		
	}
	
	public String getAdmin_phone() {
		return admin_phone;
	}
	public void setAdmin_phone(String admin_phone) {
		this.admin_phone = admin_phone;
	}
	public String getAdmin_name() {
		return admin_name;
	}
	public void setAdmin_name(String admin_name) {
		this.admin_name = admin_name;
	}
	public String getAdmin_password() {
		return admin_password;
	}
	public void setAdmin_password(String admin_password) {
		this.admin_password = admin_password;
	}
	public String getAdmin_birthday() {
		return admin_birthday;
	}
	public void setAdmin_birthday(String admin_birthday) {
		this.admin_birthday = admin_birthday;
	}
	public String getAdmin_sex() {
		return admin_sex;
	}
	public void setAdmin_sex(String admin_sex) {
		this.admin_sex = admin_sex;
	}
	public String getAdmin_email() {
		return admin_email;
	}
	public void setAdmin_email(String admin_email) {
		this.admin_email = admin_email;
	}
}
